package weather;

public class Temp {
    Float day;
    Float min;
    Float max;
    Float night;
    Float eve;
    Float morn;

    public Float getDay() {
        return day;
    }

    public Float getMin() {
        return min;
    }

    public Float getMax() {
        return max;
    }

    public Float getNight() {
        return night;
    }

    public Float getEve() {
        return eve;
    }

    public Float getMorn() {
        return morn;
    }
}
