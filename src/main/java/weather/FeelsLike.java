package weather;

public class FeelsLike {
    Float day;
    Float night;
    Float eve;
    Float morn;

    public Float getDay() {
        return day;
    }

    public Float getNight() {
        return night;
    }

    public Float getEve() {
        return eve;
    }

    public Float getMorn() {
        return morn;
    }
}
